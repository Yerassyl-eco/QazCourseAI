// Lightweight i18n for QazCourse AI. Kazakh default, Russian optional.
import { useEffect, useState } from "react";

export type Lang = "kk" | "ru";

const KEY = "qazcourse.lang";

export function getLang(): Lang {
  if (typeof window === "undefined") return "kk";
  const v = (localStorage.getItem(KEY) as Lang | null) ?? "kk";
  return v === "ru" ? "ru" : "kk";
}

export function setLang(l: Lang) {
  if (typeof window === "undefined") return;
  localStorage.setItem(KEY, l);
  document.documentElement.lang = l;
  window.dispatchEvent(new CustomEvent("qazcourse:lang"));
}

export function useLang(): [Lang, (l: Lang) => void] {
  const [lang, set] = useState<Lang>("kk");
  useEffect(() => {
    set(getLang());
    document.documentElement.lang = getLang();
    const sync = () => set(getLang());
    window.addEventListener("qazcourse:lang", sync);
    return () => window.removeEventListener("qazcourse:lang", sync);
  }, []);
  return [lang, setLang];
}

